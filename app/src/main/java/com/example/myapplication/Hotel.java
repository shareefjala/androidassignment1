package com.example.myapplication;

public class Hotel {
    public String name;
    public String location;
    public double price;
    public float rating;
    public String desc;

    public Hotel(String name, String location, double price, float rating, String desc) {
        this.name = name;
        this.location = location;
        this.price = price;
        this.rating = rating;
        this.desc = desc;
    }
}
